/// Spacing & sizing scale for the Alif mobile app.
///
/// Based on a 4px base unit. Values are doubles so they can be used directly
/// with Flutter layout APIs (`EdgeInsets`, `SizedBox`, etc.).
class SpacingScale {
  SpacingScale._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 16;
  static const double lg = 24;
  static const double xl = 32;
  static const double xxl = 48;
}
